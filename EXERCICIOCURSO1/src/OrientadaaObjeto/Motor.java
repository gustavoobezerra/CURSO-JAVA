package OrientadaaObjeto;

/**
 * Representa o motor de um carro, com controle sobre o fator de injeção e o estado de ligado/desligado.
 */
public class Motor {
    /**
     * O fator de injeção de combustível do motor.
     */
    public double fatorInjecao = 1.0;

    /**
     * O estado do motor (ligado ou desligado).
     */
    public boolean ligado = false;
}
